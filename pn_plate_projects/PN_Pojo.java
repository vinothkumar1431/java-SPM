
package pn_plate_projects;


public class PN_Pojo {
    
   private String uname;
    
   private String pass;

    public PN_Pojo(String uname, String pass) {
        this.uname = uname;
        this.pass = pass;
    }
    
    public PN_Pojo() {
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
    
    
}
